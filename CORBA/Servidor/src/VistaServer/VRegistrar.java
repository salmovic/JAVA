    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if (dtsRequeridos()) 
        {
            dts.setIdEmpelado(txtClaveEmpleado.getText());
            dts.setNombre(txtNombre.getText());
            dts.setApellidos(txtApellidos.getText());
            dts.setDireccion(txtDireccion.getText());
            dts.setTelefono(txtTelefono.getText());
            dts.setNomUsuario(txtUsuario.getText());
            dts.setPasswd(txtPasswd.getText());
            try {
            File f = new File(ruta);                
            int len=(int) f.length();
            FileInputStream fis = new FileInputStream(f);
            byte[] img = new byte[len];
            BufferedInputStream bis = new BufferedInputStream(fis);
            bis.read(img);
            ByteArrayInputStream bais = new ByteArrayInputStream(img);
            dts.setImagen(bais);
            dts.setTamImagen(len);
                if (opDs.setEmpleadoServidor(dts)) {
                    JOptionPane.showMessageDialog(rootPane, "Se realizo la operacion con exito");
                    limpiarTabla();
                    llenarTabla();
                } else {
                    JOptionPane.showMessageDialog(null, "No se pudo realizar la operacion");
                }

            } catch (Exception e) {
            }

            
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

