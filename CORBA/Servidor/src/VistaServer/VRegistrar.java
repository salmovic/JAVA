    boolean dtsRequeridos() {
        if (txtClaveEmpleado.getText().equals("")) {
            txtClaveEmpleado.setBackground(Color.red);
            txtClaveEmpleado.requestFocus();
            return false;
        }
        if (txtNombre.getText().equals("")) {
            txtNombre.setBackground(Color.red);
            txtNombre.requestFocus();
            return false;
        }
        if (txtApellidos.getText().equals("")) {
            txtApellidos.setBackground(Color.red);
            txtApellidos.requestFocus();
            return false;
        }
        if (txtUsuario.getText().equals("")) {
            txtUsuario.setBackground(Color.red);
            txtUsuario.requestFocus();
            return false;
        }
        if (txtPasswd.getText().equals("")) {
            txtPasswd.setBackground(Color.yellow);
            txtPasswd.requestFocus();
            return false;
        }
        if (ruta == null) {
            lblFoto.setForeground(Color.red);            
            JOptionPane.showMessageDialog(null, "Debe Cargar una imagen");
            cargarImagen();
            return false;
        }
        return true;
    }
