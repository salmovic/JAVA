    private void btnRAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRAceptarActionPerformed
        txtRotraCantidad.getText();
        if (txtRotraCantidad.getText().equals(evt)) {
          txtRotraCantidad.setBackground(Color.red);
          txtRotraCantidad.requestFocus();
          return;
        }
        con.conectarServidor();
        if (con.objDist.retiro(noCuenta, Double.parseDouble(txtRotraCantidad.getText()))) {
            System.out.println(tipoUsr);
            con.datosSucursal(noCuenta,"Retiro", tipoUsr);
            JOptionPane.showMessageDialog(rootPane, "Se realizo con exito");            
        }else
        {
            JOptionPane.showMessageDialog(rootPane, "No se pudo realizar la operacion");
        }
        
    }//GEN-LAST:event_btnRAceptarActionPerformed

