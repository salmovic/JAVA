    private void btnTAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTAceptarActionPerformed
        if (txtTCantidad.getText().equals("")||txtTTelefono.getText().equals("") ){
            JOptionPane.showMessageDialog(rootPane, "Todos los datos son requeridos");
            return;
        }
        
        if (cmbCompania.getSelectedIndex()==0) {
            JOptionPane.showMessageDialog(rootPane, "Seleccione una compaia");
            cmbCompania.requestFocus();
            return;
        }
        con.conectarServidor();
        if (con.objDist.tiempoAire(noCuenta,Double.parseDouble(txtTCantidad.getText()))) {
            con.datosSucursal(noCuenta,"Tiempo Aire", tipoUsr);
            JOptionPane.showMessageDialog(rootPane, "Se realizo con exito");            
        }else
        {
            JOptionPane.showMessageDialog(rootPane, "No se pudo realizar la operacion");
        }
    }//GEN-LAST:event_btnTAceptarActionPerformed

    private void btnPPagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPPagarActionPerformed
        if (txtPNoCuenta.getText().equals("")||txtPagoServicio.getText().equals("")) {
            JOptionPane.showMessageDialog(null,"Todos los datos son requeridos");
            return;
        }
        if (cbxPSelecComp.getSelectedIndex()==0) {
            JOptionPane.showMessageDialog(null,"Debe seleccionar un servicio");
            return;
        }
        con.conectarServidor();
        if (con.objDist.pagoServicio(noCuenta, Double.parseDouble(txtPagoServicio.getText()))) {
            con.datosSucursal(noCuenta,"Pago Servicio", tipoUsr);
            JOptionPane.showMessageDialog(null,"Se realizo con exito la operacion");
            
        }else
            JOptionPane.showMessageDialog(null,"No se pudo realizar la operacion");
    }//GEN-LAST:event_btnPPagarActionPerformed
